# Basic Numeric Operation
"""
1. +
2. -
3. *
4. /
5. // (Floor)
6. ** (Exponential)
"""

print(100+10)  # using '+' operator

print(100-10)  # using '-' operator

print(100*10)  # using '*' operator

print(100/10)  # using '/' operator

print(100//10)  # using '//' operator

print(100**2)  # using '**' operator
