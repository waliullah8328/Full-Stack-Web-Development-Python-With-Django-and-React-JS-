# Quotation in Python
# We have three types of quotation just like 1.('A'), 2.("A"), 3.("'A'")
# 1.Number
if 5 > 2:
    print('5 is grater than 2')  # just like 1.('A')

# 2.Number
if 5 > 2:
    print("5 is grater than 2")  # just like 2.("A")

# 3.Number
if 5 > 2:
    print("'5 is grater than 2'") # just like 3.("'A'")